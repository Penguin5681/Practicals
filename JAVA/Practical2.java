public class Practical2 {
    public static void main(String[] args) {
        int a = 12;
        int b = 6;

        // Addition
        System.out.println(a + b);

        // Subtraction
        System.out.println(a - b);
        
        // Multiplication
        System.out.println(a * b);

        // Division
        System.out.println(a / b);

        // Modulo (returns the remainder upon division of two numbers)
        System.out.println(a % b);
    }
}
